
<template>
	<view class="content">
		<form class="app-update-pv">

			<view class="btn">
				<button
					:style='{"boxShadow":"0 0 16rpx rgba(0,0,0,0) inset","backgroundColor":"#FE8010","borderColor":"#409EFF","borderRadius":"8rpx","color":"rgba(255, 255, 255, 1)","borderWidth":"0","width":"70%","fontSize":"32rpx","borderStyle":"solid","height":"80rpx"}'
					@tap="onSubmitTap" class="bg-red">提交</button>
			</view>
		</form>

					<w-picker mode="dateTime" step="1" :current="false" :hasSecond="false" @confirm="insertTimeConfirm"
						  ref="insertTime" themeColor="#333333"></w-picker>
					<w-picker mode="dateTime" step="1" :current="false" :hasSecond="false" @confirm="createTimeConfirm"
						  ref="createTime" themeColor="#333333"></w-picker>


	</view>
</template>

<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				cross: '',
                ro:{
					goodsId: false,
					yonghuId: false,
					goodsCollectionTypes: false,
					insertTime: false,
					createTime: false,
				},
				ruleForm: {
					goodsId: '',
					yonghuId: '',
					goodsCollectionTypes: '',//数字
					goodsCollectionValue: '',//数字对应的值
					insertTime: '',
					createTime: '',
				},
				// 登陆用户信息
				user: {},
				goodsCollectionTypesOptions: [],
				goodsCollectionTypesIndex : 0,

			}
		},
		components: {
			wPicker
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},
		},
		async onLoad(options) {

		/*下拉框*/
			let goodsCollectionTypesParams = {
				page: 1,
				limit: 100,
				dicCode: 'goods_collection_types',
			}
			let goodsCollectionTypes = await this.$api.page(`dictionary`, goodsCollectionTypesParams);
			this.goodsCollectionTypesOptions = goodsCollectionTypes.data.list


			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				let res = await this.$api.info(`goodsCollection`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			if(options.goodsCollectionId){
                this.ruleForm.goodsCollectionId = options.goodsCollectionId;
			}
			// 跨表
			// this.styleChange()
		},
		methods: {
			// 下拉变化
            goodsCollectionTypesChange(e) {
            this.goodsCollectionTypesIndex = e.target.value
            this.ruleForm.goodsCollectionValue = this.goodsCollectionTypesOptions[this.goodsCollectionTypesIndex].indexName
            this.ruleForm.goodsCollectionTypes = this.goodsCollectionTypesOptions[this.goodsCollectionTypesIndex].codeIndex
        },


			// styleChange() {
			// 	this.$nextTick(() => {
			// 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
			// 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
			// 		// })
			// 	})
			// },


			// 日期控件
			insertTimeConfirm(val) {
                console.log(val)
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                console.log(val)
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },




			getUUID() {
				return new Date().getTime();
			},
			async onSubmitTap() {
				if (this.ruleForm.id) {
					await this.$api.update(`goodsCollection`, this.ruleForm);
				} else {
					await this.$api.save(`goodsCollection`, this.ruleForm);
				}
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

